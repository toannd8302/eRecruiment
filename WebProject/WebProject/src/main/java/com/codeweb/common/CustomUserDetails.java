package com.codeweb.common;

import com.codeweb.security.*;
import com.codeweb.pojos.department;
import java.util.ArrayList;
import java.util.Collection;
import java.util.List;
import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

/**
 *
 * @author KHOA
 */
public class CustomUserDetails implements UserDetails{

    private department department;

    public CustomUserDetails(department department) {
        this.department = department;
    }
    
    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        List<GrantedAuthority> authorities = new ArrayList<>();
        authorities.add(new SimpleGrantedAuthority(department.getRole()));
        return authorities;
    }

    @Override
    public String getPassword() {
        return department.getPassword();
    }

    @Override
    public String getUsername() {
        return department.getEmail();
    }

    @Override
    public boolean isAccountNonExpired() {
        return true;
    }

    @Override
    public boolean isAccountNonLocked() {
        return true;
    }

    @Override
    public boolean isCredentialsNonExpired() {
        return true;
    }

    @Override
    public boolean isEnabled() {
        return true;
    }
    
}
